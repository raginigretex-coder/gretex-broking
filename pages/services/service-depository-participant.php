                        <details class="equity-faq-item">
                            <summary>What is a Depository Participant (DP)?</summary>
                            <p>A DP is an agent of a central depository (such as NSDL or CDSL) that offers demat services—opening and maintaining demat accounts, processing dematerialisation and rematerialisation, and executing depository instructions on your behalf.</p>
                        </details>
                        <details class="equity-faq-item">
                            <summary>Do I need a demat account to trade in equities?</summary>
                            <p>For delivery-based equity trades in dematerialised form, holdings are held in a demat account. Your trading member and DP may be the same or different entities depending on your arrangement.</p>
                        </details>
                        <details class="equity-faq-item">
                            <summary>What is dematerialisation?</summary>
                            <p>Dematerialisation is the process of converting physical share certificates into electronic balances in your demat account. It requires submission of certificates and prescribed forms through your DP.</p>
                        </details>
                        <details class="equity-faq-item">
                            <summary>What is a pledge in demat?</summary>
                            <p>A pledge allows you to mark securities in your demat account as collateral in favour of a pledgee (for example, for loans), as per depository procedures. Closure of pledge releases the securities when obligations are met.</p>
                        </details>
                    </div>
                </div>
                <div class="equity-faq-group">
                    <div class="equity-faq-accordion">
