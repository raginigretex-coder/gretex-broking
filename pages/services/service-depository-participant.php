                            <summary>How do corporate actions reflect in my demat account?</summary>
                            <p>Bonus, split, merger, and similar events are credited to eligible demat accounts based on depository and issuer records, generally without physical paperwork where electronic credit is applicable.</p>
                        </details>
                        <details class="equity-faq-item">
                            <summary>Can I transfer my demat account from another DP?</summary>
                            <p>Yes, account closure cum transfer or inter-depository transfer processes exist. We can guide you on forms, timelines, and charges as per depository guidelines.</p>
